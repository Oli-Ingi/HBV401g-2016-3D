                currentTour = (Tour)tourList.getSelectedValue();
                
                secondaryTitleLab.setText(currentTour.getName());
                tourTypeLab.setText("Type: "+currentTour.getType());
                tourPriceLab.setText("Price: "+priceToString(currentTour.getPrice()));
